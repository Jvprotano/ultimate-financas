export function useFinancas() {
  const [storedScenarios, setScenarios] = useLocalStorage<FinanceScenario[]>(
    SCENARIOS_STORAGE_KEY,
    loadInitialScenarios(),
  )
  const scenarios = useMemo(
    () => (Array.isArray(storedScenarios) ? storedScenarios.map(normalizeScenario) : []),
    [storedScenarios],
  )
  const [activeScenarioId, setActiveScenarioId] = useLocalStorage<string>(
    ACTIVE_SCENARIO_STORAGE_KEY,
    loadInitialActiveScenarioId(scenarios),
  )

  useEffect(() => {
    if (scenarios.length === 0) {
      const scenario = createDefaultScenario('Atual')
      setScenarios([scenario])
      setActiveScenarioId(scenario.id)
      return
    }

    if (!scenarios.some((scenario) => scenario.id === activeScenarioId)) {
      setActiveScenarioId(scenarios[0].id)
    }
  }, [activeScenarioId, scenarios, setActiveScenarioId, setScenarios])

  const activeScenario =
    scenarios.find((scenario) => scenario.id === activeScenarioId) ?? scenarios[0] ?? createDefaultScenario('Atual')
  const activeId = activeScenario?.id ?? ''

  const updateActiveScenario = useCallback(
    (updater: (scenario: FinanceScenario) => FinanceScenario) => {
      setScenarios((prev) =>
        prev.map((scenario) =>
          scenario.id === activeId
            ? { ...updater(normalizeScenario(scenario)), updatedAt: nowIso() }
            : scenario,
        ),
      )
    },
    [activeId, setScenarios],
  )

  const setScenarioField = useCallback(
    <K extends keyof FinanceScenarioData>(field: K, value: FinanceScenarioData[K]) => {
      updateActiveScenario((scenario) => ({ ...scenario, [field]: value }))
    },
    [updateActiveScenario],
  )

  // Cenários
  const createScenario = useCallback(
    (name = `Cenário ${scenarios.length + 1}`) => {
      const scenario = createDefaultScenario(name)
      setScenarios((prev) => [...prev, scenario])
      setActiveScenarioId(scenario.id)
    },
    [scenarios.length, setActiveScenarioId, setScenarios],
  )

  const duplicateScenario = useCallback(
    (sourceId = activeId) => {
      const source = scenarios.find((scenario) => scenario.id === sourceId) ?? activeScenario
      if (!source) return
      const scenario = cloneScenario(source, `${source.name} (cópia)`)
      setScenarios((prev) => [...prev, scenario])
      setActiveScenarioId(scenario.id)
    },
    [activeId, activeScenario, scenarios, setActiveScenarioId, setScenarios],
  )

  const renameScenario = useCallback(
    (id: string, name: string) => {
      const trimmed = name.trim()
      if (!trimmed) return
      setScenarios((prev) =>
        prev.map((scenario) =>
          scenario.id === id ? { ...scenario, name: trimmed, updatedAt: nowIso() } : scenario,
        ),
      )
    },
    [setScenarios],
  )

  const removeScenario = useCallback(
    (id: string) => {
      if (scenarios.length <= 1) return
      const remaining = scenarios.filter((scenario) => scenario.id !== id)
      setScenarios(remaining)
      if (id === activeId) {
        setActiveScenarioId(remaining[0]?.id ?? '')
      }
    },
    [activeId, scenarios, setActiveScenarioId, setScenarios],
  )

  // Custos
  const addCost = useCallback(
    (name: string, value: number, category: CostCategory) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        costs: [...scenario.costs, { id: uid(), name, value, category }],
      }))
    },
    [updateActiveScenario],
  )

  const removeCost = useCallback(
    (id: string) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        costs: scenario.costs.filter((c) => c.id !== id),
      }))
    },
    [updateActiveScenario],
  )

  // Desejos
  const addWant = useCallback(
    (name: string, plannedAmount = 0) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        wants: [...scenario.wants, { id: uid(), name, plannedAmount: Math.max(0, plannedAmount) }],
      }))
    },
    [updateActiveScenario],
  )

  const removeWant = useCallback(
    (id: string) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        wants: scenario.wants.filter((w) => w.id !== id),
      }))
    },
    [updateActiveScenario],
  )

  const updateWantAmount = useCallback(
    (id: string, plannedAmount: number) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        wants: scenario.wants.map((w) =>
          w.id === id ? { ...w, plannedAmount: Math.max(0, plannedAmount) } : w,
        ),
      }))
    },
    [updateActiveScenario],
  )

  // Descontos em folha
  const addDeduction = useCallback(
    (name: string, value: number, type: DeductionType, employerContribution = 0) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        deductions: [...scenario.deductions, { id: uid(), name, value, type, employerContribution }],
      }))
    },
    [updateActiveScenario],
  )

  const removeDeduction = useCallback(
    (id: string) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        deductions: scenario.deductions.filter((d) => d.id !== id),
      }))
    },
    [updateActiveScenario],
  )

  const updateDeductionEmployerContribution = useCallback(
    (id: string, employerContribution: number) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        deductions: scenario.deductions.map((d) =>
          d.id === id ? { ...d, employerContribution: Math.max(0, employerContribution) } : d,
        ),
      }))
    },
    [updateActiveScenario],
  )

  // Diversificação
  const updateDiversification = useCallback(
    (slices: DiversificationSlice[]) => {
      setScenarioField('diversification', slices)
    },
    [setScenarioField],
  )

  const addDiversificationSlice = useCallback(
    (name: string, percentage: number, color: string) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        diversification: [...scenario.diversification, { id: uid(), name, percentage, color }],
      }))
    },
    [updateActiveScenario],
  )

  const removeDiversificationSlice = useCallback(
    (id: string) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        diversification: scenario.diversification.filter((s) => s.id !== id),
      }))
    },
    [updateActiveScenario],
  )

  // Reserva de emergência
  const setEmergencyFundCurrent = useCallback(
    (value: number) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        emergencyFund: { ...scenario.emergencyFund, current: Math.max(0, value) },
      }))
    },
    [updateActiveScenario],
  )

  const setEmergencyFundTargetMonths = useCallback(
    (months: number) => {
      updateActiveScenario((scenario) => ({
        ...scenario,
        emergencyFund: { ...scenario.emergencyFund, targetMonths: Math.max(1, Math.round(months)) },
      }))
    },
    [updateActiveScenario],
  )

  // Cartões de crédito (inalterado)
  const addCreditCardEntry = useCallback(
    (entry: Omit<CreditCardEntry, 'id'>) => {
      updateActiveScenario((scenario) => {
        const nextEntries = [...scenario.creditCardEntries, normalizeCreditCardEntry({ ...entry, id: uid() })]
        return {
          ...scenario,
          creditCardEntries: entry.cycle === 'current' ? syncGeneratedNextEntries(nextEntries) : nextEntries,
        }
      })
    },
    [updateActiveScenario],
  )

  const updateCreditCardEntry = useCallback(
    (id: string, patch: Partial<Omit<CreditCardEntry, 'id'>>) => {
      updateActiveScenario((scenario) => {
        const target = scenario.creditCardEntries.find((entry) => entry.id === id)
        if (!target) return scenario

        // Editar um lançamento gerado automaticamente o torna manual,
        // para a edição não ser descartada na próxima sincronização.
        const effectivePatch =
          target.cycle === 'next' && target.autoGenerated ? { ...patch, autoGenerated: false } : patch
        const nextEntries = scenario.creditCardEntries.map((entry) =>
          entry.id === id ? normalizeCreditCardEntry({ ...entry, ...effectivePatch }) : entry,
        )

        return {
          ...scenario,
          creditCardEntries: target.cycle === 'current' ? syncGeneratedNextEntries(nextEntries) : nextEntries,
        }
      })
    },
    [updateActiveScenario],
  )

  const removeCreditCardEntry = useCallback(
    (id: string) => {
      updateActiveScenario((scenario) => {
        const target = scenario.creditCardEntries.find((entry) => entry.id === id)
        const nextEntries = scenario.creditCardEntries.filter((entry) => entry.id !== id)
        return {
          ...scenario,
          creditCardEntries: target?.cycle === 'current' ? syncGeneratedNextEntries(nextEntries) : nextEntries,
        }
      })
    },
    [updateActiveScenario],
  )

  const replaceCreditCardEntries = useCallback(
    (cycle: CreditCardCycle, entries: Omit<CreditCardEntry, 'id' | 'cycle'>[]) => {
      updateActiveScenario((scenario) => {
        const nextEntries = [
          ...scenario.creditCardEntries.filter((entry) => entry.cycle !== cycle),
          ...entries.map((entry) => normalizeCreditCardEntry({ ...entry, cycle, id: uid() })),
        ]
        return {
          ...scenario,
          creditCardEntries: cycle === 'current' ? syncGeneratedNextEntries(nextEntries) : nextEntries,
        }
      })
    },
    [updateActiveScenario],
  )

  const appendCreditCardEntries = useCallback(
    (cycle: CreditCardCycle, entries: Omit<CreditCardEntry, 'id' | 'cycle'>[]) => {
      updateActiveScenario((scenario) => {
        const nextEntries = [
          ...scenario.creditCardEntries,
          ...entries.map((entry) => normalizeCreditCardEntry({ ...entry, cycle, id: uid() })),
        ]
        return {
          ...scenario,
          creditCardEntries: cycle === 'current' ? syncGeneratedNextEntries(nextEntries) : nextEntries,
        }
      })
    },
    [updateActiveScenario],
  )

  const anticipateCreditCardInstallments = useCallback(
    (id: string, count: number) => {
      updateActiveScenario((scenario) => {
        const entry = scenario.creditCardEntries.find((item) => item.id === id)
        if (!entry || entry.cycle !== 'current' || !entry.installmentCurrent || !entry.installmentTotal) {
          return scenario
        }

        const current = entry.installmentCurrent
        const total = entry.installmentTotal
        const quantity = Math.min(Math.max(1, Math.floor(count)), total - current)
        if (quantity < 1) return scenario

        // Cada parcela antecipada vira um lançamento próprio na fatura atual;
        // o valor restante da compra fica concentrado na última parcela lançada.
        const anticipated = Array.from({ length: quantity }, (_, index) => {
          const installment = current + index + 1
          return normalizeCreditCardEntry({
            ...entry,
            id: uid(),
            installmentCurrent: installment,
            remainingAmount:
              installment === current + quantity
                ? buildRemainingInstallmentsAmount(entry.amount, installment, total)
                : 0,
          })
        })

        const nextEntries = [
          ...scenario.creditCardEntries.map((item) => (item.id === id ? { ...item, remainingAmount: 0 } : item)),
          ...anticipated,
        ]

        return { ...scenario, creditCardEntries: syncGeneratedNextEntries(nextEntries) }
      })
    },
    [updateActiveScenario],
  )

  const payCreditCardInvoice = useCallback(() => {
    updateActiveScenario((scenario) => {
      // Sincroniza antes de virar o ciclo: dados criados antes da geração
      // automática podem ainda não ter as parcelas materializadas na próxima.
      const syncedEntries = syncGeneratedNextEntries(scenario.creditCardEntries)
      const newCurrentEntries = syncedEntries
        .filter((entry) => entry.cycle === 'next')
        .map((entry) =>
          normalizeCreditCardEntry({
            ...entry,
            id: uid(),
            cycle: 'current',
            autoGenerated: false,
            sourceEntryId: undefined,
          }),
        )

      return { ...scenario, creditCardEntries: syncGeneratedNextEntries(newCurrentEntries) }
    })
  }, [updateActiveScenario])

  const setCreditCardSettings = useCallback(
    (settings: CreditCardSettings) => {
      setScenarioField('creditCardSettings', {
        ...settings,
        personalSpendingLimit: Math.max(0, settings.personalSpendingLimit),
      })
    },
    [setScenarioField],
  )

  const metrics = useMemo(() => calculateScenario(activeScenario), [activeScenario])

  const scenarioSummaries = useMemo<ScenarioSummary[]>(
    () =>
      scenarios.map((scenario) => {
        const summary = calculateScenario(scenario)
        return {
          id: scenario.id,
          name: scenario.name,
          availableForBudget: summary.availableForBudget,
          totalCosts: summary.totalCosts,
          totalWantsAmount: summary.totalWantsAmount,
          totalPlannedInvestment: summary.totalPlannedInvestment,
          balanceAfterPlan: summary.balanceAfterPlan,
          savingsRate: summary.savingsRate,
        }
      }),
    [scenarios],
  )

  return {
    // Cenários
    scenarios,
    activeScenario,
    activeScenarioId: activeId,
    setActiveScenarioId,
    createScenario,
    duplicateScenario,
    renameScenario,
    removeScenario,
    scenarioSummaries,

    // Estado bruto do cenário ativo
    salaryNet: activeScenario.salaryNet,
    setSalaryNet: (value: number) => setScenarioField('salaryNet', value),
    salaryInputMode: activeScenario.salaryInputMode,
    setSalaryInputMode: (mode: SalaryInputMode) => setScenarioField('salaryInputMode', mode),
    costs: activeScenario.costs,
    addCost,
    removeCost,
    wants: activeScenario.wants,
    addWant,
    removeWant,
    updateWantAmount,
    deductions: activeScenario.deductions,
    addDeduction,
    removeDeduction,
    updateDeductionEmployerContribution,
    selectedModelId: activeScenario.selectedModelId,
    setSelectedModelId: (id: string) => setScenarioField('selectedModelId', id),
    customModel: activeScenario.customModel,
    setCustomModel: (model: { n: number; d: number; i: number }) => setScenarioField('customModel', model),
    diversification: activeScenario.diversification,
    updateDiversification,
    addDiversificationSlice,
    removeDiversificationSlice,
    emergencyFund: activeScenario.emergencyFund,
    setEmergencyFundCurrent,
    setEmergencyFundTargetMonths,

    // Métricas calculadas
    metrics,

    // Cartões de crédito
    creditCardEntries: activeScenario.creditCardEntries,
    creditCardSettings: activeScenario.creditCardSettings,
    creditCardSummary: metrics.creditCardSummary,
    addCreditCardEntry,
    updateCreditCardEntry,
    removeCreditCardEntry,
    replaceCreditCardEntries,
    appendCreditCardEntries,
    anticipateCreditCardInstallments,
    payCreditCardInvoice,
    setCreditCardSettings,
  }
}
